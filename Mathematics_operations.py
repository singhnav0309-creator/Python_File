""" Perform Basic Mathematical Operations """

num1 = input("Enter a first number: ")
num2 = input("Enter a second number: ")
print(num1)
print(num2)

# Addition
Result = int(num1) + int(num2)
print(Result)

# Subtraction
Result = int(num1) - int(num2)
print(Result)

# Multiplication
Result = int(num1) * int(num2)
print(Result)

# Division
Result = int(num1) / int(num2)
print(Result)







